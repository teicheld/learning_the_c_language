int		ft_operation_add(int a, int b)
{
	return (a + b);
}

int		ft_operation_minus(int a, int b)
{
	return (a - b);
}

int		ft_operation_devide(int a, int b)
{
	return (a / b);
}

int		ft_operation_multiply(int a, int b)
{
	return (a * b);
}

int		ft_operation_modulo(int a, int b)
{
	return (a % b);
}
